    elseif (igas == 2)  #   O2
        buf[ko] = 2
    elseif (igas == 3)  #   CO2
        buf[kc] = 1
        buf[ko] = 2
    elseif (igas == 4)  #   H2O
        buf[kh] = 2
        buf[ko] = 1
    elseif (igas == 5)  #   Ar

    elseif (igas == 11)  #   CH4
        buf[kc] = 1
        buf[kh] = 4
    elseif (igas == 12)  #   C2H6
        buf[kc] = 2
        buf[kh] = 6
    elseif (igas == 13)  #   C3H8
        buf[kc] = 3
        buf[kh] = 8
    elseif (igas == 14)  #   C4H10
        buf[kc] = 4
        buf[kh] = 10
    elseif (igas == 18)  #   C8H18
        buf[kc] = 8
        buf[kh] = 18
    elseif (igas == 24)  #   C14H30
        buf[kc] = 14
        buf[kh] = 30
    elseif (igas == 32)  #   C2H5OH
        buf[kc] = 2
        buf[kh] = 6
        buf[ko] = 1
        buf[kn] = 0
    elseif (igas == 322431)   #   Fuel 32 blend with fuel 24 with 31% blend of the first fuel
        buf[kc] = 6  #6.5543
        buf[kh] = 14 #14.8424
        buf[ko] = 1  #1
        buf[kn] = 0  #0
