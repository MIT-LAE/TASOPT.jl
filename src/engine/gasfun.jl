using Zygote

"""
    gasfun(igas, t)

Computes properties of a thermally-perfect gas
with some variable specific heat cp[T].

!!! details "๐ Inputs and Outputs"
    **Input:**
    - `igas`: index specifying the gas (see if blocks below for list)
    - `t`: temperature T in Kelvin

    **Output:**
    - `s`: entropy-complement function s[T]
    - `s_t`: ds/dT
    - `h`: complete enthalpy function h[T]
    - `h_t`: dh/dT
    - `cp`: specific heat cp[T]
    - `r`: ideal-gas constant R


The adiabatic pressure change over a process 1->2 with some polytropic efficiency epol is:

``\\ p2  = \\ p1  exp [   epol   (s2-s1)/R ] ``   compression

``\\ p2  = \\ p1  exp [ (1/epol) (s2-s1)/R ] ``   expansion
"""
function gasfun(igas, t)
    
    if (igas == 1)
        s, h, cp, r = gas_N2(t)
    elseif (igas == 2)
        s, h, cp, r = gas_O2(t)
    elseif (igas == 3)
        s, h, cp, r = gas_CO2(t)
    elseif (igas == 4)
        s, h, cp, r = gas_H2O(t)
    elseif (igas == 5)
        s, h, cp, r = gas_Ar(t)
    elseif (igas == 11)
        s, h, cp, r = gas_CH4(t)
    elseif (igas == 12)
        s, h, cp, r = gas_C2H6(t)
    elseif (igas == 13)
        s, h, cp, r = gas_C3H8(t)
    elseif (igas == 14)
        s, h, cp, r = gas_C4H10(t)
    elseif (igas == 18)
        s, h, cp, r = gas_C8H18(t)
    elseif (igas == 24)
        s, h, cp, r = gas_C14H30(t)
    elseif (igas == 32)
        s, h, cp, r = gas_C2H5OH(t)
    elseif (igas == 322431)#Fuel 32 blend with fuel 24 with 31% blend of the first fuel
        s, h, cp, r = gas_C2H5OHJetA31Blend(t)
    elseif (igas == 322429)#Fuel 32 blend with fuel 24 with 29% blend of the first fuel
        s, h, cp, r = gas_C2H5OHJetA29Blend(t)    
    elseif (igas == 322471)#Fuel 32 blend with fuel 24 with 71% blend of the first fuel
        s, h, cp, r = gas_C2H5OHJetA71Blend(t)    
    elseif (igas == 40)
        s, h, cp, r = gas_H2(t)
    else
        error("GASFUN: undefined gas index: ", igas, " & code=0")
    end
    s_t = cp / t
    h_t = cp
    return s, s_t, h, h_t, cp, r
end # gasfun

"""
    gaschem(igas)

Returns number of C,H,O,N atoms in gas molecule, for the gases implemented in function gasfun above.

!!! details "๐ Inputs and Outputs"
    **Input:**
    - `igas`: index specifying the gas (see if blocks below for list)

    **Output:**
    - `nchon(.)`: number of C,H,O,N atoms in gas molecule

"""
function gaschem(igas)

    kc = 1
    kh = 2
    ko = 3
    kn = 4

    nchon = [0, 0, 0, 0]
    buf = Zygote.Buffer(nchon, length(nchon))
    for i = 1:length(nchon)
